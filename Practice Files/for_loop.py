for item in 'Python':
    print(item)

for item in ['Mosh', 'John', 'Sarah']:
    print(item)

for item in [1, 2, 3]:
    print(item)

for item in range(10): #range will give 0 to 9, excluding 10
    print(item)

for item in range(5, 10): #range will give 5 to 9, excluding 10
    print(item)

for item in range(5, 10, 2):  # range will give 5, 7, 9
    print(item)